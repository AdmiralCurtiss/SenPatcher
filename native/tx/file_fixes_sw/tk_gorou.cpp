namespace SenLib::TX::FileFixesSw::tk_gorou {
std::string_view GetDescription() {
    return "Text fixes in conversations with Gorou.";
}

bool TryApply(const SenPatcher::GetCheckedFileCallback& getCheckedFile,
              std::vector<SenPatcher::P3APackFile>& result) {
    try {
        auto fileSw = FindAlreadyPackedFile(
            result,
            "scripts/talk/dat/tk_gorou.dat",
            16249,
            HyoutaUtils::Hash::SHA1FromHexString("928d229a3b98783260790a2b00acc0aa384e1826"));
        if (!fileSw || !fileSw->HasVectorData()) {
            return false;
        }

        auto bin = fileSw->GetVectorData();
        SenScriptPatcher patcher(bin);

        // "Need anything? I'm on my break"
        // missing period at the end
        bin[0xbae] = 0x2e;

        fileSw->SetVectorData(std::move(bin));
        return true;
    } catch (...) {
        return false;
    }
}
} // namespace SenLib::TX::FileFixesSw::tk_gorou
